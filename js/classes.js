class Ator{
    constructor(id, nome){
        this.id = id;
        this.nome = nome;
    }
}

class Diretor{
    constructor(id, nome){
        this.id = id;
        this.nome = nome;
    }
}

class Filme{
    constructor(id, titulo, ano, genero, cartaz, diretor, atores, classificacao, rated, sinopse, duracao,  ){
        this.id = id;
        this.titulo = titulo;
        this.ano = ano;
        this.genero = genero;
        this.cartaz = cartaz;
        this.diretor = diretor;
        this.atores = atores;
        this.classificacao = classificacao;
        this.rated = rated;
        this.sinopse = sinopse;
        this.duracao = duracao;
    }
}





